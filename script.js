// متغيرات عامة
let filteredProperties = Object.values(properties);
let currentCategory = '';
let currentSearch = '';

// العناصر DOM
const propertiesContainer = document.getElementById('propertiesContainer');
const categoryFilter = document.getElementById('categoryFilter');
const searchInput = document.getElementById('searchInput');
const modal = document.getElementById('propertyModal');
const modalContent = document.getElementById('modalContent');
const closeModal = document.querySelector('.close');

// تهيئة التطبيق
document.addEventListener('DOMContentLoaded', function() {
    displayProperties(filteredProperties);
    setupEventListeners();
});

// إعداد مستمعي الأحداث
function setupEventListeners() {
    categoryFilter.addEventListener('change', filterProperties);
    searchInput.addEventListener('input', filterProperties);
    closeModal.addEventListener('click', hideModal);
    
    // إغلاق النافذة المنبثقة عند النقر خارجها
    window.addEventListener('click', function(event) {
        if (event.target === modal) {
            hideModal();
        }
    });
}

// عرض العقارات
function displayProperties(propertiesToShow) {
    if (propertiesToShow.length === 0) {
        propertiesContainer.innerHTML = `
            <div class="no-results">
                <h3>😔 لا توجد عقارات تطابق البحث</h3>
                <p>جرب تغيير فلترات البحث أو إزالة بعض الكلمات</p>
            </div>
        `;
        return;
    }

    propertiesContainer.innerHTML = propertiesToShow.map(property => `
        <div class="property-card" onclick="showPropertyDetails(${property.id})">
            <div class="property-image" style="background: linear-gradient(45deg, #667eea, #764ba2); display: flex; align-items: center; justify-content: center; color: white; font-size: 3rem;">
                ${getCategoryIcon(property.category)}
            </div>
            <div class="property-content">
                <h3 class="property-title">${property.title}</h3>
                <div class="property-location">${property.location}</div>
                <div class="property-category">${property.category}</div>
                <div class="property-description">${property.description.substring(0, 150)}...</div>
                <div class="property-price">${property.price}</div>
            </div>
        </div>
    `).join('');
}

// الحصول على أيقونة التصنيف
function getCategoryIcon(category) {
    const icons = {
        'فلل': '🏘️',
        'عمائر': '🏢',
        'أراضي': '🏞️',
        'شقق': '🏠',
        'استراحات': '🏕️'
    };
    return icons[category] || '🏠';
}

// تصفية العقارات
function filterProperties() {
    currentCategory = categoryFilter.value;
    currentSearch = searchInput.value.toLowerCase().trim();
    
    filteredProperties = Object.values(properties).filter(property => {
        const matchesCategory = !currentCategory || property.category === currentCategory;
        const matchesSearch = !currentSearch || 
            property.title.toLowerCase().includes(currentSearch) ||
            property.location.toLowerCase().includes(currentSearch) ||
            property.description.toLowerCase().includes(currentSearch);
        
        return matchesCategory && matchesSearch;
    });
    
    displayProperties(filteredProperties);
}

// عرض تفاصيل العقار
function showPropertyDetails(propertyId) {
    const property = properties[propertyId];
    if (!property) return;
    
    modalContent.innerHTML = `
        <div class="modal-image" style="background: linear-gradient(45deg, #667eea, #764ba2); display: flex; align-items: center; justify-content: center; color: white; font-size: 5rem;">
            ${getCategoryIcon(property.category)}
        </div>
        <h2 class="modal-title">${property.title}</h2>
        
        <div class="modal-details">
            <div class="modal-detail-item">
                <h4>📍 الموقع</h4>
                <p>${property.location}</p>
            </div>
            <div class="modal-detail-item">
                <h4>🏷️ التصنيف</h4>
                <p>${property.category}</p>
            </div>
            <div class="modal-detail-item">
                <h4>💰 السعر</h4>
                <p style="font-weight: bold; color: #2c5530;">${property.price}</p>
            </div>
        </div>
        
        <div class="modal-description">
            <h4>📋 تفاصيل العقار:</h4>
            <p>${property.description}</p>
        </div>
        
        <div style="text-align: center; margin-top: 2rem; padding: 1rem; background: #f8f9fa; border-radius: 10px;">
            <p style="color: #666; margin-bottom: 1rem;">للاستفسار والتواصل</p>
            <p style="font-weight: bold; color: #333;">📞 اتصل بنا للمزيد من التفاصيل</p>
        </div>
    `;
    
    modal.style.display = 'block';
    document.body.style.overflow = 'hidden';
}

// إخفاء النافذة المنبثقة
function hideModal() {
    modal.style.display = 'none';
    document.body.style.overflow = 'auto';
}

// إضافة تأثيرات التمرير السلس
document.addEventListener('scroll', function() {
    const cards = document.querySelectorAll('.property-card');
    cards.forEach(card => {
        const rect = card.getBoundingClientRect();
        const isVisible = rect.top < window.innerHeight && rect.bottom > 0;
        
        if (isVisible) {
            card.style.opacity = '1';
            card.style.transform = 'translateY(0)';
        }
    });
});

// تحسين الأداء للبحث
let searchTimeout;
searchInput.addEventListener('input', function() {
    clearTimeout(searchTimeout);
    searchTimeout = setTimeout(filterProperties, 300);
});